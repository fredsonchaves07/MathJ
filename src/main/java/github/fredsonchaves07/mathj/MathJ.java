package github.fredsonchaves07.mathj;

public final class MathJ {

    public static int sum(int ... values) {
        return Arithmetic.sum(values);
    }

    public static double sum(double ... values) {
        return Arithmetic.sum(values);
    }

    public static int sub(int ... values) {
        return Arithmetic.sub(values);
    }

    public static double sub(double ... values) {
        return Arithmetic.sub(values);
    }

    public static int mult(int ... numbers) {
        return Arithmetic.mult(numbers);
    }

    public static double mult(double ... numbers) {
        return Arithmetic.mult(numbers);
    }

    public static int div(int ... numbers) {
        return Arithmetic.div(numbers);
    }

    public static double div(double ... numbers) {
        return Arithmetic.div(numbers);
    }
}